package section3.exerciseFunction2;

public class Comensal {

	private String name;
	private double salary;
	private int table;
	
	public Comensal(String name, double salary, int table) {
		super();
		this.name = name;
		this.salary = salary;
		this.table = table;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getSalary() {
		return salary;
	}

	public void setSalary(double salary) {
		this.salary = salary;
	}

	public int getTable() {
		return table;
	}

	public void setTable(int table) {
		this.table = table;
	}
	
	
	
}
